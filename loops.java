package JAVA;

public class loops {
    public static void main(String[] args) {
        // FOR LOOP
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        // WHILE LOOP
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }

        // DO WHILE LOOP
        int x = 1;
        do {
            System.out.println(x);
            x++;
        } while (x <= 10);
    }
}
